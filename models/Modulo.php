<?php 

namespace Model;

class Modulo extends ActiveRecord{
    protected static $tabla = 'modulos';
    protected static $columnasDB = ['id', 'nombre'];

    public $id;
    public $nombre;

    public function __construct($args = [])
    {
        $this->id = $args['id'] ?? null;
        $this->nombre = $args['nombre'] ?? '';
    }
}